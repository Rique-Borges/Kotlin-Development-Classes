package com.example.kotlinbasics

fun main(){
    val nome = 13
    var idade : Byte = 40
    println("Hello World! my name is $nome and i am $idade years old")
    idade = 127
    println("Hello World! my name also is $nome but i am $idade years old")
    val e = 2.12345678910
    val eFloat = 2.12345678910f
    println("$e is a double, and $eFloat is a float")
}
